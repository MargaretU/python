from math import sqrt

a1 = float(input('Ввведіть значення радіусу орбіти першої планети '))
T1 = float(input('Ввведіть період обертання першої планети '))
a2 = float(input('Ввведіть значення радіусу орбіти другої планети '))
if a1 > 0 and a2 > 0:
    T2 = float(sqrt((T1**2*a2**3)/(a1**3)))
    print('Період обертання планети навколо Сонця: ', round(T2, 3))
else:
    print('Дані не задовільняють умову ')